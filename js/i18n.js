// Initialize language on DOM ready and wire up selectors
const initI18nSelectors = () => {
  console.log("initI18nSelectors called");
  const storedLang = localStorage.getItem('geohogar_lang') || 'es';
  window.changeLanguage(storedLang);

  // Setup click listeners for language buttons to show/hide dropdowns
  const setupLangSelector = (btnId, dropdownId, containerId) => {
    const btn = document.getElementById(btnId);
    const dropdown = document.getElementById(dropdownId);
    const container = document.getElementById(containerId);
    if (!btn || !dropdown || !container) {
      console.log(`setupLangSelector: Element not found - btnId: ${btnId}, dropdownId: ${dropdownId}, containerId: ${containerId}`);
      return;
    }
    console.log(`setupLangSelector: Element found, attaching listeners - btnId: ${btnId}`);

    btn.addEventListener('click', (e) => {
      e.preventDefault();
      e.stopPropagation();
      const isExpanded = btn.getAttribute('aria-expanded') === 'true';
      console.log(`Lang selector button clicked: ${btnId}, current isExpanded: ${isExpanded}`);
      
      // Close all other dropdowns first
      document.querySelectorAll('.lang-dropdown.show, .curr-dropdown.show').forEach(d => {
        if (d !== dropdown) {
          console.log(`Closing other dropdown:`, d.id);
          d.classList.remove('show');
        }
      });
      document.querySelectorAll('.lang-selector').forEach(s => {
        if (s !== container) s.classList.remove('active');
        const b = s.querySelector('.lang-btn');
        if (b && b !== btn) b.setAttribute('aria-expanded', 'false');
      });

      // Toggle current
      btn.setAttribute('aria-expanded', !isExpanded);
      container.classList.toggle('active');
      dropdown.classList.toggle('show');
      console.log(`Toggled classes: active=${container.classList.contains('active')}, show=${dropdown.classList.contains('show')}`);
    });

    // Option selection
    dropdown.querySelectorAll('.lang-opt').forEach(opt => {
      opt.addEventListener('click', (e) => {
        e.stopPropagation();
        const selectedLang = opt.getAttribute('data-lang');
        console.log(`Language option clicked: ${selectedLang} on dropdown ${dropdownId}`);
        if (selectedLang) {
          window.changeLanguage(selectedLang);
        }
        
        // Hide dropdown
        btn.setAttribute('aria-expanded', 'false');
        container.classList.remove('active');
        dropdown.classList.remove('show');
      });
    });
  };

  setupLangSelector('topbar-lang-btn', 'topbar-lang-dropdown', 'topbar-lang-selector');
  setupLangSelector('sidebar-lang-btn', 'sidebar-lang-dropdown', 'sidebar-lang-selector');

  // Close selectors on clicking outside
  document.addEventListener('click', () => {
    document.querySelectorAll('.lang-dropdown').forEach(d => d.classList.remove('show'));
    document.querySelectorAll('.lang-selector').forEach(s => {
      s.classList.remove('active');
      const b = s.querySelector('.lang-btn');
      if (b) b.setAttribute('aria-expanded', 'false');
    });
  });

  // Sync active language class in selectors initially and on change
  const syncSelectorActiveClasses = (lang) => {
    document.querySelectorAll('.lang-opt').forEach(opt => {
      if (opt.getAttribute('data-lang') === lang) {
        opt.classList.add('active');
      } else {
        opt.classList.remove('active');
      }
    });
  };

  syncSelectorActiveClasses(storedLang);
  document.addEventListener('geohogar:lang:changed', (e) => {
    syncSelectorActiveClasses(e.detail.lang);
  });
};

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18nSelectors);
} else {
  initI18nSelectors();
}

