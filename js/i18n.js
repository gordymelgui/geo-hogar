// Traduce textos que llegan hardcoded en español desde Firestore
// al idioma activo de la UI. Usa patrones heurísticos de coincidencia.

window.translateNotificationText = function(text) {
  if (!text || typeof text !== 'string') return text || '';
  const lang = window.currentLang || 'es';
  if (lang === 'es') return text; // Ya está en español

  const t = window.t || function(k) { return k; };

  const patterns = [
    { re: /^"?(.+?)"? bajó (\d+)%\s?[—\-]\s?ahora US\$\s?([\d,.]+)$/, fn: (m) => t('notif_price_drop_body').replace('{title}', m[1]).replace('{drop}', m[2]).replace('{price}', m[3]) },
    { re: /^Se publicó un nuevo (.+?) en tu zona de interés(.*)$/, fn: (m) => t('notif_new_listing_body').replace('{type}', m[1]).replace('{maxPrice}', m[2] || '') },
    { re: /^"?(.+?)"? ha sido RESERVADA por el propietario\.$/, fn: (m) => t('notif_fav_status_body').replace('{title}', m[1]) },
    { re: /^"?(.+?)"? bajó un (\d+)% y ahora cuesta US\$\s?([\d,.]+)$/, fn: (m) => t('notif_fav_drop_body').replace('{title}', m[1]).replace('{price}', m[3]) },
    { re: /^El vendedor actualizó las fotos y descripción de tu favorito "?(.+?)"?\.$/, fn: (m) => t('notif_fav_update_body').replace('{title}', m[1]) },
    { re: /^El precio promedio del m² en (.+?) subió un (\d+)%/, fn: (m) => t('notif_market_rise_body').replace('{zone}', m[1]).replace('{rise}', m[2]) },
    { re: /^(.+?) te envió un mensaje nuevo$/, fn: (m) => t('notif_new_message_from').replace('{sender}', m[1]) },
    { re: /^Nueva propiedad:\s(.+?)\sen\s(.+?)\spor US\$\s?([\d,.]+)$/, fn: (m) => t('notif_new_property_added').replace('{title}', m[1]).replace('{address}', m[2]).replace('{price}', m[3]) },
    { re: /^Tu propiedad guardada bajó de precio en (.+)$/, fn: (m) => t('notif_fav_price_drop_in_zone').replace('{zone}', m[1]) },
  ];

  for (const { re, fn } of patterns) {
    const m = text.match(re);
    if (m) {
      try { return fn(m); } catch(e) { /* sigue al siguiente patrón */ }
    }
  }

  return text;
};

window.translateNotificationTime = function(time) {
  if (!time || typeof time !== 'string') return time || '';
  const lang = window.currentLang || 'es';
  if (lang === 'es') return time;

  const t = window.t || function(k) { return k; };

  if (/^ahora mismo$/i.test(time)) return t('notif_time_now');
  const minsMatch = time.match(/^hace (\d+) min$/i);
  if (minsMatch) return t('notif_time_mins_ago').replace('{count}', minsMatch[1]);
  if (/^hace 1 hora$/i.test(time)) return t('notif_time_hour_ago');
  const hoursMatch = time.match(/^hace (\d+) horas$/i);
  if (hoursMatch) return t('notif_time_hours_ago').replace('{count}', hoursMatch[1]);

  return time;
};

